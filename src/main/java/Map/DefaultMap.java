package Map;

/**
 * The DefaultMap class represents the default map used in the game. It inherits from the Map class.
 */
public class DefaultMap extends Map {
    /**
     * Constructs a new DefaultMap object with the specified world size.
     *
     * @param worldSize the size of the world.
     */
    public DefaultMap(int worldSize) {
        super(worldSize);
    }
}
